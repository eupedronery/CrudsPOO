package sobremesa;

public class Sobremesa {
	
	private int id = 0;
	private String nome = "";
	private double preço = 0.0;
	
	public int getId() {
		return id;
	}
	
	public void setId(int id) {
		this.id = id;
	}

	public String getNome() {
		return nome;
	}
	
	public void setNome(String sabor) {
		this.nome = sabor;
	}
	
	
	public Double getPreço() {
		return preço;
	}
	
	public void setPreço(double preço) {
		this.preço = preço;
	}

}

